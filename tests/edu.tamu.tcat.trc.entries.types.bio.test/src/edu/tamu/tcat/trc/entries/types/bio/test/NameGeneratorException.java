package edu.tamu.tcat.trc.entries.types.bio.test;

public class NameGeneratorException extends Exception
{

   public NameGeneratorException()
   {
   }

   public NameGeneratorException(String message)
   {
      super(message);
   }

   public NameGeneratorException(Throwable cause)
   {
      super(cause);
   }

   public NameGeneratorException(String message, Throwable cause)
   {
      super(message, cause);
   }

   public NameGeneratorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
   {
      super(message, cause, enableSuppression, writableStackTrace);
   }

}
